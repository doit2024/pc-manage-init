<template>
  <div>
    <div class="demo-upload-list" v-for="(item, i) in imageList" :key="i">
      <!-- <template> -->
        <img :src="item">
        <div class="demo-upload-list-cover">
          <Icon type="ios-eye-outline" @click.native="handleView(i)"></Icon>
        </div>
      <!-- </template> -->
    </div>
    <div v-if="!imageList.length">无</div>
    <Modal title="图片预览" v-model="visible">
      <img :src="imageList[active]" v-if="visible" style="width: 100%">
    </Modal>
  </div>
</template>
<script>
export default {
  props: {
    image: {
      type: Array | String,
      require: true
    }
  },
  data () {
    return {
      visible: false,
      active: 1,
      setting: {
        autoplay: false,
        autoplaySpeed: 2000,
        dots: 'inside',
        radiusDot: false,
        trigger: 'click',
        arrow: 'hover'
      }
    }
  },
  computed: {
    imageList () {
      let image = this.image
      return image ? (typeof image === 'string' ? [image] : image) : []
    }
  },
  methods: {
    handleView (index) {
      this.active = index
      this.visible = true
    }
  }
}
</script>
